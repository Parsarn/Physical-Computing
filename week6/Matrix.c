#include <stdio.h>
 
int main(){
    double n[3][3], m[3][3], ans[3][3];
    for (int i = 0 ; i < 3; i++){
        for (int j = 0 ; j < 3; j++){
            scanf(" %lf", &n[i][j]);
        }
    }
 
    for (int i = 0 ; i < 3; i++){
        for (int j = 0 ; j < 3; j++){
            scanf(" %lf", &m[i][j]);
        }
    }
    ans[0][0] = (n[0][0] * m[0][0]) + (n[0][1] * m[1][0]) + (n[0][2] * m[2][0]);
    ans[0][1] = (n[0][0] * m[0][1]) + (n[0][1] * m[1][1]) + (n[0][2] * m[2][1]);
    ans[0][2] = (n[0][0] * m[0][2]) + (n[0][1] * m[1][2]) + (n[0][2] * m[2][2]);
     
    ans[1][0] = (n[1][0] * m[0][0]) + (n[1][1] * m[1][0]) + (n[1][2] * m[2][0]);
    ans[1][1] = (n[1][0] * m[0][1]) + (n[1][1] * m[1][1]) + (n[1][2] * m[2][1]);
    ans[1][2] = (n[1][0] * m[0][2]) + (n[1][1] * m[1][2]) + (n[1][2] * m[2][2]);
     
    ans[2][0] = (n[2][0] * m[0][0]) + (n[2][1] * m[1][0]) + (n[2][2] * m[2][0]);
    ans[2][1] = (n[2][0] * m[0][1]) + (n[2][1] * m[1][1]) + (n[2][2] * m[2][1]);
    ans[2][2] = (n[2][0] * m[0][2]) + (n[2][1] * m[1][2]) + (n[2][2] * m[2][2]);
     
    printf("A x B\n");
    for (int i = 0 ; i < 3; i++){
        for (int j = 0 ; j < 3; j++){
            printf("%.2lf ", ans[i][j]);
        }
        printf("\n");
    }
 
    return 0;
}